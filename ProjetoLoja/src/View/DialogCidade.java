     private void iniciaComponentes(){
       texto_id.setText("");
       texto_nome.setText("");
       texto_pesquisa.setText("");
       combo_estado.setSelectedIndex(0);
       combo_pais.setSelectedIndex(0);
       texto_nome.requestFocus();
   }
   private Cidade populateObject() throws NumberFormatException{
        return new Cidade(
        texto_id.getText().isEmpty()?1:Integer.parseInt(texto_id.getText()),
        texto_nome.getText(),
        (Estado)combo_estado.getSelectedItem(),
        (Pais)combo_pais.getSelectedItem());  
    }
   
   private void populateComponentes(Cidade cidade){
        texto_id.setText(cidade.getId()+"");
        texto_nome.setText(cidade.getNome());
        combo_estado.setSelectedItem(cidade.getEstado()); 
        combo_pais.setSelectedItem(cidade.getPais()); 
    }

